package youtube.data.niche;

public class Media {
    private String url;
    private int width;
    private int height;

    /**
     * @return String return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @return int return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * @return int return the height
     */
    public int getHeight() {
        return height;
    }

}
